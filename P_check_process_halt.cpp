int process_halt(Grid3D& options_table,  int size)
{
	int i = 0, j = 0, set2 = 0, max_set=0;

	//find empty cell error
	for (i = 0;i < size;i++)
	{
		for (j = 0;j < size;j++)
		{
			if (options_table(i,j,0) > 0)
			{
				if (options_table(i,j,0) == 2) { set2++; }
				else { max_set++; }
			}
		}
	}
	
	if (max_set == 0)
	{
		if (set2 > 0) { cout << " puzzle solve and puzzle has multiple solutions" << endl; }
		else { cout << "Puzzle solved and puzzle has unique solution " << endl; }
		return  2;
	}
	else if (set2 == 0) 
	{
		cout << " Two symbol set not found process will terminate" << endl;
		return  2;
	}
	else { return 1; }	
}
