int build_options_table(Grid3D& p_OT_1, Grid2D& table, int size, int blk_size)
{
	int i = 0, j = 0, k = 0, id;


	//build options symbol list for each cell
	//initalise
	for(i = 0; i < size; i++)
	{
		for(j = 0; j < size; j++)
		{
			if (table(i,j) == 0)
			{
				id = 0;
				build_options_table_fx(p_OT_1, table, i, j, size, blk_size);
			}
			else
			{
				for (k = 0;k < size + 1;k++) { p_OT_1(i,j,k) = 0;}
			}

			p_OT_1(i,j,size+1) = 0;
			p_OT_1(i,j,size + 2) = 0;
			p_OT_1(i,j,size + 3) = 0;
			p_OT_1(i,j,size + 4) = 0;
		
		}
	}
	return 0;
}
