int build_options_table_fx(Grid3D& p_OT_1, Grid2D& table, int x, int y, int size, int blk_size)
{
	int i, j, s, bx = (x / blk_size) * blk_size, by = (y / blk_size) * blk_size;

	for (i = 0;i < size + 1;i++) { p_OT_1(x,y,i) = i; }

	for (i = 0;i < size;i++)
	{
		if (table(i,y) > 0) { s = table(i,y); p_OT_1(x,y,s) = 0; }
	}
	for (j = 0;j < size;j++)
	{
		if (table(x,j) > 0) { s = table(x,j); p_OT_1(x,y,s) = 0; }
	}
	for (i = bx;i < bx+blk_size;i++)
	{
		for (j = by;j < by + blk_size;j++)
		{
			if (table(i,j) > 0) { s = table(i,j); p_OT_1(x,y,s) = 0; }
		}
	}

	j = 0;
	for (i = 1;i < size + 1;i++)
	{
		if (p_OT_1(x,y,i) > 0) { j++; }
	}

	p_OT_1(x,y,0) = j;
	return 0;

}
