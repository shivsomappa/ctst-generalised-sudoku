int m3_Randomise_sudoku_rc_shift(Grid2D& table,int size,int blk_size)
{
	int i=0;
	for(i=0;i<size;i=i+blk_size)
	{
		m3_Row_shift_within_blk(table, size,i, blk_size);
	}

	for(i=0;i<size;i=i+blk_size)
	{
		m3_Column_shift_within_blk(table, size,i, blk_size);
	}

	return 0;
}
